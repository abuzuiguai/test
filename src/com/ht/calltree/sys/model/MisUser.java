package com.ht.calltree.sys.model;

public class MisUser {
	private String staffId;
	private String accountName;
	private String chineseName;
	private String phoneNo;
	private String pinYin;
	private String primaryProjectID;
	private String primaryProjectName;
	private String primaryRT;

	public String getStaffId() {
		return staffId;
	}

	public void setStaffId(String staffId) {
		this.staffId = staffId;
	}

	public String getAccountName() {
		return accountName;
	}

	public void setAccountName(String accountName) {
		this.accountName = accountName;
	}

	public String getChineseName() {
		return chineseName;
	}

	public void setChineseName(String chineseName) {
		this.chineseName = chineseName;
	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}

	public String getPinYin() {
		return pinYin;
	}

	public void setPinYin(String pinYin) {
		this.pinYin = pinYin;
	}

	public String getPrimaryProjectID() {
		return primaryProjectID;
	}

	public void setPrimaryProjectID(String primaryProjectID) {
		this.primaryProjectID = primaryProjectID;
	}

	public String getPrimaryProjectName() {
		return primaryProjectName;
	}

	public void setPrimaryProjectName(String primaryProjectName) {
		this.primaryProjectName = primaryProjectName;
	}

	public String getPrimaryRT() {
		return primaryRT;
	}

	public void setPrimaryRT(String primaryRT) {
		this.primaryRT = primaryRT;
	}
}
